import React, { Component } from "react";
// import { render } from 'react-dom';

class Map extends Component {
    constructor(props) {
        super(props);
        console.log(props);
        this.onScriptLoad = this.onScriptLoad.bind(this);
    }
    componentDidMount() {
      // no window google object -> not loaded and mounted yet.
        if (!window.google) {
          // create script tag for Google API
            var s = document.createElement("script");
            s.type = "text/javascript";
            s.src = `https://maps.google.com/maps/api/js?key=${key}`;
           //=YOUR_API_KEY

            var x = document.getElementsByTagName("script")[0];
            x.parentNode.insertBefore(s, x);
            
            // Below is important.
            //We cannot access google.maps until it's finished loading
            s.addEventListener("load", e => {
                this.onScriptLoad();
            });
        } else {
          // can call script load because google map api is already loaded and accessible by window.google object.
            this.onScriptLoad();
        }
    }
    onScriptLoad = () => {
        const map = new window.google.maps.Map (
            document.getElementById(this.props.id),
            {
              options : this.props.options,
              styles : styles
            }
           
        );
        this.props.onMapLoad(map);
    };
    
    render() {
      return (
        <div style={{ width: '100%', height: '100%' }} id={this.props.id} />
      );
    }
}

export default Map;


